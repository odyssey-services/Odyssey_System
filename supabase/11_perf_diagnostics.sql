    coalesce((entry.value->>'sort_order')::integer, entry.ordinality - 1)
  from jsonb_array_elements(
    case
      when jsonb_typeof(p_payload->'skills') = 'array' then p_payload->'skills'
      else '[]'::jsonb
    end
  ) with ordinality as entry(value, ordinality)
  where trim(coalesce(entry.value->>'name', '')) <> '';
  get diagnostics v_row_count = row_count;
  v_duration_ms := (extract(epoch from clock_timestamp() - v_phase_started_at) * 1000)::numeric(12,2);
  perform public.odyssey_debug_perf(
    p_function_name => v_function_name,
    p_span_name => 'insert-skills',
    p_phase_name => 'insert-skills',
    p_trace_id => v_trace_id,
    p_character_id => v_character_id,
    p_character_key => v_character_key,
    p_room_id => v_room_id,
