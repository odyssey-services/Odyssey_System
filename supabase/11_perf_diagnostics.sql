  id bigint,
  trace_id text,
  parent_id text,
  function_name text,
  span_name text,
  phase_name text,
  level text,
  duration_ms numeric,
  character_id uuid,
  actor_character_id uuid,
  target_character_id uuid,
  character_key text,
  room_id text,
  scene_id text,
  metadata jsonb,
  error_code text,
  created_at timestamptz
)
language sql
as $body$
  select
    log.id,
    log.trace_id,
    log.parent_id,
    log.function_name,
    log.span_name,
    log.phase_name,
    log.level,
    log.duration_ms,
    log.character_id,
