  v_duration_ms := (extract(epoch from clock_timestamp() - v_phase_started_at) * 1000)::numeric(12,2);
  perform public.odyssey_debug_perf(
    p_function_name => v_function_name,
    p_span_name => 'upsert-character',
    p_phase_name => 'upsert-character',
    p_trace_id => v_trace_id,
    p_character_id => v_character_id,
    p_character_key => v_character_key,
    p_room_id => v_room_id,
    p_duration_ms => v_duration_ms,
    p_metadata => jsonb_build_object(
      'character_bucket', v_character_bucket,
      'tracker_minor', v_tracker_minor,
      'tracker_serious', v_tracker_serious
    )
  );

  v_phase_started_at := clock_timestamp();
  delete from public.odyssey_character_characteristics where character_id = v_character_id;
  delete from public.odyssey_character_skills where character_id = v_character_id;
  delete from public.odyssey_character_weapons where character_id = v_character_id;
  delete from public.odyssey_character_body_parts where character_id = v_character_id;
  v_duration_ms := (extract(epoch from clock_timestamp() - v_phase_started_at) * 1000)::numeric(12,2);
  perform public.odyssey_debug_perf(
    p_function_name => v_function_name,
    p_span_name => 'clear-child-records',
    p_phase_name => 'clear-child-records',
    p_trace_id => v_trace_id,
    p_character_id => v_character_id,
    p_character_key => v_character_key,
    p_room_id => v_room_id,
    p_duration_ms => v_duration_ms
  );

  v_phase_started_at := clock_timestamp();
  insert into public.odyssey_character_characteristics (
    character_id,
    key,
    label,
    value,
    sort_order
