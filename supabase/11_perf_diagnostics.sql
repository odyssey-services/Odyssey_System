create or replace function public.odyssey_debug_perf(
  p_function_name text,
  p_span_name text,
  p_phase_name text default '',
  p_trace_id text default '',
  p_parent_id text default '',
  p_level text default 'info',
  p_duration_ms numeric default 0,
  p_character_id uuid default null,
  p_actor_character_id uuid default null,
  p_target_character_id uuid default null,
  p_character_key text default '',
  p_room_id text default '',
  p_scene_id text default '',
  p_metadata jsonb default '{}'::jsonb,
  p_error_code text default ''
)
returns void
language plpgsql
as $body$
begin
  insert into public.odyssey_debug_perf_log (
    trace_id,
    parent_id,
    function_name,
    span_name,
    phase_name,
    level,
    duration_ms,
    character_id,
    actor_character_id,
    target_character_id,
    character_key,
    room_id,
    scene_id,
    metadata,
    error_code
  )
  values (
    trim(coalesce(p_trace_id, '')),
    trim(coalesce(p_parent_id, '')),
    coalesce(nullif(trim(coalesce(p_function_name, '')), ''), 'unknown_function'),
    coalesce(nullif(trim(coalesce(p_span_name, '')), ''), 'unknown_span'),
    trim(coalesce(p_phase_name, '')),
    case lower(coalesce(p_level, 'info'))
      when 'error' then 'error'
      when 'warn' then 'warn'
      else 'info'
    end,
    greatest(0, coalesce(p_duration_ms, 0)),
    p_character_id,
    p_actor_character_id,
    p_target_character_id,
    trim(coalesce(p_character_key, '')),
    trim(coalesce(p_room_id, '')),
    trim(coalesce(p_scene_id, '')),
    case
      when jsonb_typeof(p_metadata) = 'object' then p_metadata
      when p_metadata is null then '{}'::jsonb
      else jsonb_build_object('value', p_metadata)
    end,
    trim(coalesce(p_error_code, ''))
  );
end;
$body$;

create or replace function public.get_odyssey_debug_perf_log(
  p_limit integer default 200,
  p_trace_id text default null
)
