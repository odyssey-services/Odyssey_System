  v_resources jsonb := case
    when jsonb_typeof(p_payload->'resources') = 'object' then p_payload->'resources'
    else '{}'::jsonb
  end;
  v_result jsonb := '{}'::jsonb;
begin
  v_phase_started_at := clock_timestamp();
  if v_character_key = '' then
    raise exception 'character_key is required';
  end if;
  v_duration_ms := (extract(epoch from clock_timestamp() - v_phase_started_at) * 1000)::numeric(12,2);
  perform public.odyssey_debug_perf(
    p_function_name => v_function_name,
    p_span_name => 'validate',
    p_phase_name => 'validate',
    p_trace_id => v_trace_id,
    p_character_key => v_character_key,
    p_room_id => v_room_id,
    p_duration_ms => v_duration_ms,
    p_metadata => jsonb_build_object(
      'character_bucket', v_character_bucket,
      'enabled', v_enabled
    )
  );

  v_phase_started_at := clock_timestamp();
  insert into public.odyssey_characters (
    character_key,
    character_bucket,
    source_template_key,
    enabled,
    tracker_minor,
    tracker_serious,
    owner_player_id,
    owner_player_name,
    campaign_id,
    room_id,
    resources,
    is_deleted
  )
  values (
    v_character_key,
    v_character_bucket,
    v_source_template_key,
    v_enabled,
    v_tracker_minor,
    v_tracker_serious,
    v_owner_player_id,
    v_owner_player_name,
    v_campaign_id,
    v_room_id,
    v_resources,
    false
